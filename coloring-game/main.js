//MAKES THE ART!
function paint() {
  var isDown = false;
  $(document).mousedown(function () {
    isDown = true;
  })
    .mouseup(function () {
      isDown = false;
    })
  $('.cell').mouseover(function () {
    if (isDown) {
      $(this).css('background-color', color, 'fast')
    }
  })
}

